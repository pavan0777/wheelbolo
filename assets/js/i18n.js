  function fmt(str, vars) {
    if (!vars) return str;
    return str.replace(/\{(\w+)\}/g, (m, k) => (k in vars ? vars[k] : m));
  }

  const I18n = {
    lang: 'en',
    STRINGS: STRINGS,
    LANGS: LANGS,
    has(code) { return !!(code && STRINGS[code]); },
    getLang(code) { return LANGS.find((l) => l.code === code) || null; },
    isRtl(code) { const l = this.getLang(code || this.lang); return !!(l && l.rtl); },
    t(key, vars) {
      const dict = STRINGS[this.lang] || STRINGS.en;
      const s = dict[key] != null ? dict[key] : (STRINGS.en[key] != null ? STRINGS.en[key] : key);
      return fmt(s, vars);
    },
    apply(root) {
      root = root || document;
      root.querySelectorAll('[data-i18n]').forEach((el) => {
        el.textContent = this.t(el.getAttribute('data-i18n'));
      });
      root.querySelectorAll('[data-i18n-attr]').forEach((el) => {
        el.getAttribute('data-i18n-attr').split(';').forEach((pair) => {
          const [attr, key] = pair.split(':').map((s) => s.trim());
          if (attr && key) el.setAttribute(attr, this.t(key));
        });
      });
    },
    set(lang) {
      this.lang = STRINGS[lang] ? lang : 'en';
      document.documentElement.lang = this.lang;
      document.documentElement.dir = this.isRtl(this.lang) ? 'rtl' : 'ltr';
      this.apply(document);
      document.dispatchEvent(new CustomEvent('i18n:change', { detail: { lang: this.lang } }));
    }
  };

  window.I18n = I18n;
